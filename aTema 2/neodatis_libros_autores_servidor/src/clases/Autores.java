package clases;

import java.util.ArrayList;

/**
 *
 * @author a16pablovc
 */
public class Autores {
    
private String dni,nombre,direccion,nacionalidad;
private int edad;
    ArrayList<Libros> libros;

    public Autores(String dni, String nombre, String direccion, String nacionalidad, int edad) {
        this.dni = dni;
        this.nombre = nombre;
        this.direccion = direccion;
        this.nacionalidad = nacionalidad;
        this.edad = edad;
        this.libros = new ArrayList<>();
    }

    public Autores() {
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getNacionalidad() {
        return nacionalidad;
    }

    public void setNacionalidad(String nacionalidad) {
        this.nacionalidad = nacionalidad;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public ArrayList<Libros> getLibros() {
        return libros;
    }

    public void setLibros(ArrayList<Libros> libros) {
        this.libros = libros;
    }

    


}
